    public void atualizar() {
        try {
            DefaultTableModel model = (DefaultTableModel) avaliacao.getModel();
            model.setNumRows(0);
            listaAV = new AvaliacaoDao().getlista("AVALIACAO");
            System.out.println(listaAV.size());
            for (Avaliacao avaliacao : listaAV) {
                model.addRow(new Object[]{
                    avaliacao.getIdColaborador().getRegistro(),
                    avaliacao.getIdColaborador().getNome(),
                    avaliacao.getIdCargo().getFuncao(),
                    format.format(avaliacao.getDataAvaliacao()),
                    format.format(avaliacao.getFimExperiencia())
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            Msg.erro(this, "erro ao atualizar a tabela\nErro:" + e.getMessage());
        }
    }

