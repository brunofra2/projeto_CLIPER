    protected void atualiza(String s){
        try {
            lista = new UsuarioDao().getlista(s);
            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.setNumRows(0);
                for (Usuario u : lista) {
                   model.addRow(new Object[]{
                        u.getCodigo(),
                        u.getTipo()
                   });
            }
        } catch (Exception e) {
            e.printStackTrace();
            Msg.erro(this, "Erro ao atualizar a tabela\n Erro :"+e.getMessage());
        }
    }
