    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int row = tabela.getSelectedRow();
       if(row < 0){
           Msg.alerta(this, "selecione um registro");
       }else{
           try {
               Cadastrar_usuario u = new Cadastrar_usuario(this, true);
               u.setAlterar(true);
               u.preenche(lista.get(row));
               u.setVisible(true);
           } catch (Exception e) {
               e.printStackTrace();
               Msg.erro(this,"Erro ao preencher os campos\nErro :"+e.getMessage());
           }
       }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int row = tabela.getSelectedRow();
       if(row < 0){
           Msg.alerta(this, "selecione um registro");
       }else{
           if(Msg.confirmar(this, "Deseja realmente excluir este registro") == true){
               try {
                   new UsuarioDao().excluir(lista.get(row));
                Loggin.arquivo_log("excluido cadastro de um usuario com o id"+lista.get(row));
                   atualiza("");
                   Msg.informacao(this,"excluido com sucesso");
               } catch (Exception e) {
                    if(e.getMessage().contains("Error while committing the transaction")){
                        Msg.alerta(this, "Este registro nÃ£o pode ser excluido\n"
                                + "ele esta vinculado ha outro modulo do sistema");
                    }else{
                   e.printStackTrace();
                   Msg.erro(this, "Erro ao excluir o registro\nErro :"+e.getMessage());
                    }
               }
           }
       }
    }//GEN-LAST:event_jButton3ActionPerformed

