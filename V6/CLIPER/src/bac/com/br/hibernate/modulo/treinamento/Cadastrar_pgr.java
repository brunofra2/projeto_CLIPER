    private boolean verifica(){
        boolean f = false;
        String s = "campo vazio \n";
        if(ANO.getText().equals("")){
            f = true;
            s +="ano";
        }
        if(f == true){
            Msg.alerta(this, s);
        }
        return f;
    }
    private Pgr salvar(){
       p.setAno(ANO.getText());
       return p;
    }
    
    protected void preenchecampo(Pgr f){
        p = f;
        ANO.setText(f.getAno());
    }
    private void concluir(){
        if(isAlterar() == false){
            if(verifica() == false){
                try {
//                    new PgrDao().inserir(salvar());
                    Loggin.arquivo_log("criado cadastro de um novo ano com o id"+p.getId()+
                            " com os itens:"+p.getAno());
                    Msg.informacao(this, "registro salvo com sucesso");
                    pai.atualizar("");
                } catch (Exception e) {
                    e.printStackTrace();
                    Msg.erro(this, "erro ao inserir registro \n"+e.getMessage());
                }
            }
        }else{
            if(verifica() == false){
                try {
//                    new PgrDao().alterar(salvar());
                    Loggin.arquivo_log("alterado cadastro de um novo ano com o id"+p.getId()+
                            " com os itens:"+p.getAno());
                    Msg.informacao(this, "registro alterado com sucesso");
                } catch (Exception e) {
                    e.printStackTrace();
                    Msg.erro(this, "erro ao alterar registro \n"+e.getMessage());
                }
            }
        }
        
                    pai.atualizar("");
    }
